import React from "react";
import Snackbar from "@material-ui/core/Snackbar";
import SnackbarContent from "@material-ui/core/SnackbarContent";
import IconButton from "@material-ui/core/IconButton";
import CheckCircleIcon from "@material-ui/icons/CheckCircle";
import ErrorIcon from "@material-ui/icons/Error";
import WarningRoundedIcon from "@material-ui/icons/WarningRounded";
import classNames from "classnames";
import { withStyles } from "@material-ui/core/styles";
import CloseIcon from "@material-ui/icons/Close";

const variantIcon = {
  success: CheckCircleIcon,
  error: ErrorIcon,
  warning: WarningRoundedIcon
};

const styles = theme => ({
  success: {
    backgroundColor: theme.palette.primary.main
  },
  error: {
    backgroundColor: theme.palette.error.main
  },
  warning: {
    backgroundColor: theme.palette.warning
  },

  icon: {
    fontSize: 20
  },
  iconVariant: {
    opacity: 0.9,
    marginRight: theme.spacing.unit
  },
  message: {
    display: "flex",
    alignItems: "center",
    maxWidth: "100%"
  }
});

const ContentWrapper = props => {
  const { classes, className, message, variant, onClose } = props;
  const Icon = variantIcon[variant];
  return (
    <SnackbarContent
      className={classNames(classes[variant], className)}
      aria-describedby="client-snackbar"
      message={
        <span id="client-snackbar" className={classes.message}>
          <Icon className={classNames(classes.icon, classes.iconVariant)} />
          {message}
        </span>
      }
      action={[
        <IconButton key="close" aria-label="Close" color="inherit" className={classes.close} onClick={onClose}>
          <CloseIcon className={classes.icon} />
        </IconButton>
      ]}
      //workarround-fix: can be removed if material.ui fixed their issue (https://github.com/mui-org/material-ui/issues/13144)
      headlineMapping={{
        body1: "div",
        body2: "div"
      }}
    />
  );
};

const SnackbarContentWrapper = withStyles(styles)(ContentWrapper);

const NotificationsSnackbar = props => {
  return (
    <Snackbar open={props.showSnackbar} autoHideDuration={4000000} onClose={props.closeSnackbar}>
      <SnackbarContentWrapper
        variant={props.snackbarError ? "error" : props.snackbarWarning ? "warning" : "success"}
        message={props.snackbarMessage}
        onClose={props.closeSnackbar}
      />
    </Snackbar>
  );
};
export default withStyles(styles)(NotificationsSnackbar);
